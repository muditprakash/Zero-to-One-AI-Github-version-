@app.post("/regenerate-section")
async def regenerate_section(request: RegenerateRequest):
    if not request.api_key or not request.api_key.strip():
        raise HTTPException(status_code=400, detail="Gemini API key is required.")
    
    if request.id not in blueprints_db:
        raise HTTPException(status_code=404, detail="Blueprint not found")
    
    idea = blueprints_db[request.id]["idea"]
    current_blueprint = blueprints_db[request.id]["blueprint"]
    
    section_map = {
        "market_analysis": MarketAnalysis,
        "competitive_landscape": CompetitiveLandscape,
        "personas": List[Persona],
        "mvp_spec": MVPSpec,
        "budget_and_team": BudgetAndTeam,
        "gtm_strategy": GTMStrategy,
        "timeline": Timeline,
        "investor_suggestions": List[Investor]
    }
    
    schema = section_map.get(request.section)
    if not schema:
        raise HTTPException(status_code=400, detail="Invalid section name")
    
    prompt = f"Refine the {request.section} for the startup idea: {idea}. Context: {current_blueprint}"
    
    try:
        updated_data = await call_llm_with_retry(prompt, schema, request.api_key)
        if isinstance(updated_data, list):
            blueprints_db[request.id]["blueprint"][request.section] = [i.model_dump() for i in updated_data]
        else:
            blueprints_db[request.id]["blueprint"][request.section] = updated_data.model_dump()
            
        return blueprints_db[request.id]
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))

